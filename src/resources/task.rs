/// Derived marker component. Present when the entity's current action is
/// `Action::EngageTarget`. Maintained by `tick_actions`, read by `auto_attack`.
#[derive(Component, Clone, Copy, Debug)]
pub struct Engaging {
    pub target: Entity,
}

// ---------------------------------------------------------------------------
// AiBrain — "how do I decide what to do?" (NPC-only)
// ---------------------------------------------------------------------------

/// Which evaluator category a given evaluator belongs to.
#[derive(Clone, Copy, Debug, PartialEq, Eq)]
pub enum EvaluatorCategory {
    /// Evaluated frequently (~5 ticks). Combat-related decisions.
    Combat,
    /// Evaluated less frequently (~60 ticks). Patrol, follow, idle.
    Routine,
}

/// An evaluator type. Each variant knows how to check conditions and propose a Task.
#[derive(Clone, Debug, PartialEq)]
pub enum TaskEvaluator {
    /// Scan for hostiles, check threat table. Proposes [EngageTarget].
    EngageCombat,
    /// Check HP threshold. Proposes [FleeFrom].
    Flee,
    /// Check ability priorities/conditions. Proposes [CastAbility].
    UseAbility,
    /// Follow party leader. Proposes [FollowEntity].
    FollowLeader { leader: Entity },
    /// Defend self when attacked (threat table non-empty). Proposes [EngageTarget].
    DefendSelf,
    /// Fallback. Proposes nothing.
    Idle,
}

impl TaskEvaluator {
    pub fn category(&self) -> EvaluatorCategory {
        match self {
            Self::EngageCombat | Self::Flee | Self::UseAbility | Self::DefendSelf => {
                EvaluatorCategory::Combat
            }
            Self::FollowLeader { .. } | Self::Idle => EvaluatorCategory::Routine,
        }
    }
}

/// How often combat evaluators run (in ticks).
pub const COMBAT_EVAL_INTERVAL: u32 = 5;
/// How often routine evaluators run (in ticks).
pub const ROUTINE_EVAL_INTERVAL: u32 = 60;

/// AI decision-making brain. Only on NPC entities.
/// Holds evaluator configuration, cooldowns, and proposal staging.
#[derive(Component, Clone, Debug)]
pub struct AiBrain {
    /// Which evaluators this entity uses.
    pub evaluators: Vec<TaskEvaluator>,
    /// Ticks until next combat evaluator run.
    pub combat_eval_cooldown: u32,
    /// Ticks until next routine evaluator run.
    pub routine_eval_cooldown: u32,
    /// Proposals from evaluators this tick. Cleared each evaluation cycle.
    pub proposals: Vec<Task>,
}

impl AiBrain {
    pub fn new(evaluators: Vec<TaskEvaluator>) -> Self {
        Self {
            evaluators,
            combat_eval_cooldown: 0,
            routine_eval_cooldown: 0,
            proposals: Vec::new(),
        }
    }

    /// Standard enemy brain.
    pub fn enemy() -> Self {
        Self::new(vec![
            TaskEvaluator::Flee,
            TaskEvaluator::UseAbility,
            TaskEvaluator::EngageCombat,
            TaskEvaluator::Idle,
        ])
    }
}
